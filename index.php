                    <p>Monet left more than 500 sketches and more than 2,000 oil paintings in his life. However, the most well-known of his works is the “Water Lily” series.</p>
                    <P>There are dozens of water lily paintings in Monet’s life paintings, many of which belong to group paintings. The so-called “group paintings” are paintings made by the painter in the same position, facing the same object, and being painted at different times and in different illuminations.</P>
                    <p>What distinguishes them is just a momentary capture of light and color by the master. This is probably a distinguishing feature of Monet’s work in his later years. In the series of “Water Lilies”, from 1897 to 1926, Monet painted a total of 181 pieces.</p>
