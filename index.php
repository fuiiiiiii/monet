                </div>
                <div class="col-md-6">
                    <p>The exhibition features nearly 50 paintings by Claude Monet mainly from the year of 1913 to 1926, the final phase of his long career. During Monet’s late years, after traveling around the world, he stayed close to home. He was inspired by the variety of elements making up his own garden at Giverny, a village located about forty-five miles from Paris. With its evolving scenery of flower beds, footpaths, willows, wisteria, and nymphaea, the garden became a personal laboratory for his concentrated study of natural phenomena.</p>
                    <p>The exhibition will focus on the series that Monet invented, and just as important, reinvented, in this setting. In the process, it will reconsider the conventional notion that many of the late works painted on a large scale were preparatory for the Grand Decorations, rather than finished paintings in their own right. Boldly balancing representation and abstraction, Monet’s radical late works redefined the master of Impressionism as a forebear of modernism.</p>
